bool process_record_user(uint16_t keycode, keyrecord_t *record) {
   switch (keycode) {
      case QWERTY:
         if (record->event.pressed) {
            set_single_persistent_default_layer(_QWERTY);
            layer_move(_QWERTY);
         }
         return false;
         break;
      case COLEMAK:
         if (record->event.pressed) {
            set_single_persistent_default_layer(_COLEMAK);
            layer_move(_COLEMAK);
         }
         return false;
         break;
      case LOWER:
         if (record->event.pressed) {
            layer_on(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
         } else {
            layer_off(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
         }
         return false;
         break;
      case RAISE:
         if (record->event.pressed) {
            layer_on(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
         } else {
            layer_off(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
         }
         return false;
         break;
      case ADJUST:
         if (record->event.pressed) {
            layer_on(_ADJUST);
         } else {
            layer_off(_ADJUST);
         }
         return false;
         break;
      case ONESHOT:
         if (record->event.pressed) {
            set_oneshot_layer(_ONESHOT, ONESHOT_START);
         } else {
            clear_oneshot_layer_state(ONESHOT_PRESSED);
         }
         return false;
         break;
   }
  return true;
}